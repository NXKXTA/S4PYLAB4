from PyQt5.QtWidgets import QWidget, QGridLayout, QLabel

class Window1(QWidget):
    def __init__(self):
        super().__init__()  # Инициализация родительского класса QWidget
        self.initUI()  # Вызов метода для настройки интерфейса

    def initUI(self):
        # Создаем сетку (QGridLayout для размещения виджетов в таблице)
        layout = QGridLayout()

        # Заголовки столбцов
        layout.addWidget(QLabel("День"), 0, 0)  # Добавляем метку "День" в первую ячейку (строка 0, столбец 0)
        layout.addWidget(QLabel("Время"), 0, 1)  # Добавляем метку "Время" в первую ячейку (строка 0, столбец 1)
        layout.addWidget(QLabel("Группа 1"), 0, 2)  # Добавляем метку "Группа 1" в первую ячейку (строка 0, столбец 2)
        layout.addWidget(QLabel("Группа 2"), 0, 3)  # Добавляем метку "Группа 2" в первую ячейку (строка 0, столбец 3)

        # Данные расписания
        schedule = [
            ("Понедельник", "09:00-10:30", "Математика", "Физика"),  # Данные для первой строки расписания
            ("Понедельник", "10:45-12:15", "Информатика", "Химия"),  # Данные для второй строки расписания
            ("Вторник", "09:00-10:30", "Физика", "Математика"),  # Данные для третьей строки расписания
            ("Вторник", "10:45-12:15", "Химия", "Информатика"),  # Данные для четвертой строки расписания
        ]

        # Заполняем сетку
        for row, (day, time, group1, group2) in enumerate(schedule, start=1):  # Проходим по данным расписания
            layout.addWidget(QLabel(day), row, 0)  # Добавляем метку с днем недели в текущую строку, столбец 0
            layout.addWidget(QLabel(time), row, 1)  # Добавляем метку с временем в текущую строку, столбец 1
            layout.addWidget(QLabel(group1), row, 2)  # Добавляем метку с предметом для группы 1 в текущую строку, столбец 2
            layout.addWidget(QLabel(group2), row, 3)  # Добавляем метку с предметом для группы 2 в текущую строку, столбец 3

        # Устанавливаем layout
        self.setLayout(layout)  # Устанавливаем сетку как основной layout для окна